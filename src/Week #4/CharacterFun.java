public class CharacterFun {

    public static void main (String[] args){

        char ch = 'a';

        if (Character.isLetter(ch))
        {
            System.out.println("Yes, this is a letter!");
        }
        else
        {
            System.out.println("No, this is not a letter!");
        }

    }
}
